    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        String name = workshopNameField.getText();
        String date = workshopDateField.getText();
        String groupQty = workshopGroupQtyField.getText();
        String roomSetting = (String)workshopRoomSettingCombo.getSelectedItem(); // toString()
        String organizer = workshopOrganizerField.getText();
        String roomType = workshopRoomTypeCombo.getSelectedItem().toString();
        // TODO call from another thread + UI update from the Swing's main thread
        try {
            dbActions.addWorkshop(name, date, groupQty, roomSetting, organizer, roomType);
        } catch (java.sql.SQLException ex) {
            setErrorMessage("workshops", ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_AddActionPerformed

