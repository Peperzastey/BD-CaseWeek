    public DatabaseGUI() {
        initComponents();
        
        tabs = new TreeMap<Integer, TabInfo>();
        for (int i = 0; i < tabbedPane.getTabCount(); ++i) {
            tabs.put(i, new TabInfo(tabbedPane.getTitleAt(i)));
        }
        
        dbActions = new DBActions(this);
        
        tabbedPane.addChangeListener(new javax.swing.event.ChangeListener() {
            // tab in the TabbedPane changed
            @Override
            public void stateChanged(ChangeEvent e) {
                int i = ((javax.swing.JTabbedPane)e.getSource()).getSelectedIndex();
                TabInfo tab = tabs.get(i);
                if (!tab.initialized) {
                    if (tab.tabName.equalsIgnoreCase("workshops")) { //TEMP
                    try {
                        javax.swing.table.TableModel tableModel = DatabaseTableModel.createTableModelForDBTable(tab.tabName);
                        workshopsTable.setModel(tableModel);
                        tab.initialized = true;
                    } catch (java.sql.SQLException ex) {
                        setErrorMessage("workshops", ex.getLocalizedMessage());
                    }
                    //tab.initialized = true;
                }}
            }       
        });
    }

